package com.java.zmahfuz;

public class Loop {
    public static void main(String[] args) {

        for (int x = 1; x <= 10; x++) {
            System.out.println("value of x : " + x);

        }
        for (int x = 10; x >= 1; x--) {
            System.out.println("value of x : " + x);
        }

        }
    }




